using System;


/*  变量  
 * 一个变量只不过是一个供程序操作的存储区的名字
 * 每个变量都有一个特定的类型，类型决定了变量的内存大小和布局。范围内的值可以存储在内存中，可以对变量进行一系列操作
 * 
 * 类型               举例
 * 整数类型         sbyte、byte、short、ushort、int、uint、long、ulong 和 char
 * 浮点型          float 和 double
 * 十进制类型        decimal
 * 布尔类型         true 或 false 值，指定的值
 * 空类型          可为空值的数据类型
 * 
 */

namespace BasicKnowledge
{
    public class Variable
    {
        public void VariableInit()
        {
            int i, j, k;
            char c, ch;
            float f;
            double b;
            i = 10;
            int d = 4, m = 3;
        }
    }
}
